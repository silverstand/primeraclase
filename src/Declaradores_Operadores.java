
public class Declaradores_Operadores {

	public static void main(String[] args) {
		// Variables, Constantes y Concatenado
		
		final double apulgadas=2.54;
		double cm = 6;
		double pulgadas = cm / apulgadas;
		System.out.println("En "+ cm + " Centimetros" + " hay " + pulgadas + " pulgadas");
		
		//Ejemplo de declaracion de variables en una misma linea
		int operador1, operador2, resultado;
		operador1=10;
		operador2=30;
		resultado=operador1+operador2;
		System.out.println(resultado);
		
	}

}
